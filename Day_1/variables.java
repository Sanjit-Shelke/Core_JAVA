class variables {
    public static void main(String[] args) {
		int a=10;               // 4 bytes
		long b=20;              // 8 bytes
		float c=(float)20.10;   // 4 bytes
		double d=10.20;         // 8 bytes
		char e='J';             // 2 bytes
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
	}
}
		